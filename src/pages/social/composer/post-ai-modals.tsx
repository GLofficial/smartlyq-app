import { useState } from "react";
import { Dialog, DialogContent, DialogHeader, DialogTitle, DialogFooter } from "@/components/ui/dialog";
import { Button } from "@/components/ui/button";
import { Textarea } from "@/components/ui/textarea";
import { Input } from "@/components/ui/input";
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from "@/components/ui/select";
import { Sparkles } from "lucide-react";

/* ── AI Text Generation ─────────────────────────────────────────── */

interface AiTextProps { open: boolean; onClose: () => void; onUse: (text: string) => void }

export function AiTextModal({ open, onClose, onUse }: AiTextProps) {
	const [topic, setTopic] = useState("");
	const [contentType, setContentType] = useState("Social Media Post");
	const [tone, setTone] = useState("Professional");
	const [platform, setPlatform] = useState("facebook");
	const [generated, setGenerated] = useState("");
	const [loading, setLoading] = useState(false);

	function handleGenerate() {
		setLoading(true);
		// TODO: Wire to AI Captain API for real generation
		setTimeout(() => {
			setGenerated(`Here's a ${tone.toLowerCase()} ${contentType.toLowerCase()} about "${topic}" for ${platform}. This will be generated by AI Captain when connected.`);
			setLoading(false);
		}, 1500);
	}

	return (
		<Dialog open={open} onOpenChange={onClose}>
			<DialogContent className="max-w-lg">
				<DialogHeader><DialogTitle>AI Text Generation</DialogTitle></DialogHeader>
				<div className="space-y-4">
					<ModelBadge label="SmartlyQ AI" sub="Powered by AI Captain" gradient="from-blue-500 to-indigo-600" />
					<div>
						<Label>Content Type</Label>
						<Select value={contentType} onValueChange={setContentType}>
							<SelectTrigger className="text-sm"><SelectValue /></SelectTrigger>
							<SelectContent>
								{["Social Media Post", "Blog Post", "Ad Copy", "Product Description", "Email Newsletter"].map((t) => <SelectItem key={t} value={t}>{t}</SelectItem>)}
							</SelectContent>
						</Select>
					</div>
					<div><Label>Topic / Context</Label><Textarea value={topic} onChange={(e) => setTopic(e.target.value)} placeholder="What should the post be about?" className="min-h-[80px] text-sm" /></div>
					<div className="grid grid-cols-2 gap-3">
						<div>
							<Label>Tone</Label>
							<Select value={tone} onValueChange={setTone}>
								<SelectTrigger className="text-sm"><SelectValue /></SelectTrigger>
								<SelectContent>
									{["Professional", "Casual", "Humorous", "Inspirational", "Formal", "Persuasive"].map((t) => <SelectItem key={t} value={t}>{t}</SelectItem>)}
								</SelectContent>
							</Select>
						</div>
						<div>
							<Label>Platform</Label>
							<Select value={platform} onValueChange={setPlatform}>
								<SelectTrigger className="text-sm"><SelectValue /></SelectTrigger>
								<SelectContent>
									{["facebook", "instagram", "twitter", "linkedin", "tiktok"].map((p) => <SelectItem key={p} value={p}>{p.charAt(0).toUpperCase() + p.slice(1)}</SelectItem>)}
								</SelectContent>
							</Select>
						</div>
					</div>
					{generated && (
						<div className="rounded-lg bg-[var(--muted)]/40 p-3"><p className="text-sm whitespace-pre-wrap">{generated}</p></div>
					)}
				</div>
				<DialogFooter className="border-t pt-4 flex gap-2">
					<Button variant="outline" onClick={onClose}>Cancel</Button>
					<Button onClick={handleGenerate} disabled={!topic || loading} className="bg-gradient-to-r from-blue-500 to-indigo-600 text-white gap-1.5">
						<Sparkles size={14} /> {loading ? "Generating..." : "Generate"}
					</Button>
					{generated && <Button variant="secondary" onClick={() => { onUse(generated); onClose(); setGenerated(""); setTopic(""); }}>Use This Content</Button>}
				</DialogFooter>
			</DialogContent>
		</Dialog>
	);
}

