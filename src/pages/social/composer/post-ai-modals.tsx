/* ── Shared ──────────────────────────────────────────────────────── */

function Label({ children }: { children: React.ReactNode }) {
	return <label className="text-[10px] font-semibold text-[var(--muted-foreground)] uppercase tracking-wider mb-1.5 block">{children}</label>;
}

function ModelBadge({ label, sub, gradient }: { label: string; sub: string; gradient: string }) {
	return (
		<div className="border border-[var(--border)] rounded-lg px-4 py-3 flex items-center gap-3">
			<div className={`w-8 h-8 rounded-full bg-gradient-to-br ${gradient} flex items-center justify-center text-white text-xs font-bold`}>◆</div>
			<div><p className="text-sm font-medium">{label}</p><p className="text-xs text-[var(--muted-foreground)]">{sub}</p></div>
		</div>
	);
}
